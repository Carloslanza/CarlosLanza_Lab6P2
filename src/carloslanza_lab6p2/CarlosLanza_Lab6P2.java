    private void btn_agrearJugadorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_agrearJugadorMouseClicked
        if (btn_agrearJugador.getText().equals("Agregar")) {
            String nombre = tf_nombreJugador.getText();
            int edad = (int)spinner_edad.getValue();
            String posición = cb_posicion.getSelectedItem().toString();
            
            jugadores.add(new Jugador(nombre,edad,posición));
            llenarListaJugadores();
            
            tf_nombreJugador.setText("");
            spinner_edad.setValue(0);
            cb_posicion.setSelectedItem("Portero");

            JOptionPane.showMessageDialog(frame_crearJugadores, "Jugador creado exitosamente!");
        } else {
            String nombre = tf_nombreJugador.getText();
            for (int i = 0; i < jugadores.size(); i++) {
                if (jugadores.get(i).getNombre().equals(nombre)) {
                    Jugador jugador = jugadores.get(i);
                    jugador.setNombre(tf_nombreJugador.getText());
                    jugador.setEdad((int)spinner_edad.getValue());
                    jugador.setPosicion(cb_posicion.getSelectedItem().toString());
                    break;
                }
            }
            frame_crearJugadores.setVisible(false);
            btn_agrearJugador.setText("Agregar");
        }
    }//GEN-LAST:event_btn_agrearJugadorMouseClicked

