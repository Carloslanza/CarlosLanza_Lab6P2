    private void modificarJugador_ppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarJugador_ppActionPerformed
        String nombre = jugadores_list.getSelectedValue();
        
        if (!jugadores.isEmpty()) {
            for (int i = 0; i < jugadores.size(); i++) {
                if (jugadores.get(i).getNombre().equals(nombre)) {
                    tf_nombreJugador.setText(nombre);
                    spinner_edad.setValue(jugadores.get(i).getEdad());
                    cb_posicion.setSelectedItem(jugadores.get(i).getPosicion());
                    break;
                }
            }

            frame_transferencias.setVisible(false);
            frame_crearJugadores.setVisible(true);

            btn_agrearJugador.setText("Modificar");
        } else {
            JOptionPane.showMessageDialog(frame_transferencias, "No hay nada que modificar!");
        }
    }//GEN-LAST:event_modificarJugador_ppActionPerformed

    private void eliminarJugador_ppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarJugador_ppActionPerformed
        String nombre = jugadores_list.getSelectedValue();
        
        if (!jugadores.isEmpty()) {
            int posicion = 0;
            for (int i = 0; i < jugadores.size(); i++) {
                if (jugadores.get(i).getNombre().equals(nombre)) {
                    posicion = i;
                    break;
                }
            }
            jugadores.remove(posicion);
            llenarListaJugadores();
        } else {
            JOptionPane.showMessageDialog(frame_transferencias, "No hay nada que eliminar!");
        }
    }//GEN-LAST:event_eliminarJugador_ppActionPerformed

    public void llenarListaJugadores() {
        DefaultListModel modelo = new DefaultListModel();
        modelo.clear();
        for (int i = 0; i < jugadores.size(); i++) {
            modelo.addElement(jugadores.get(i).getNombre());
        }

        jugadores_list.setModel(modelo);
    }
